import java.util.Scanner;

public class n10869 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        double a = sc.nextInt();
        double b = sc.nextInt();
        System.out.println((int) (a + b));
        System.out.println((int) (a - b));
        System.out.println((int) (a * b));
        System.out.println((int) (a / b));
        System.out.println((int) (a % b));
    }
}
